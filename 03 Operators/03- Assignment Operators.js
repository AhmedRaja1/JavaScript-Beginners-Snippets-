// 03- Assignment Operators

/*
Theses are the assignment operator: 
    =
    +=
    -=
    *=
    /=	
    %=
*/

let x = 10; // Assigning 10 to x

console.log(x += 5); // This is the same as x = x + 5
console.log(x -= 5); // This is the same as x = x - 5
console.log(x *= 5); // This is the same as x = x * 5
console.log(x /= 5); // This is the same as x = x / 5
console.log(x %= 5); // This is the same as x = x % 5